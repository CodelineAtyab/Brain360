my_list = []
add_to_list =input("enter the items you want to add :")
while add_to_list !="done":
  my_list.append(add_to_list)
  if add_to_list != "done":
     add_to_list =input("enter the items you want to add :")

def reverse_my_list():
    return list(reversed(my_list))

print("the reversed list is:", reverse_my_list())



